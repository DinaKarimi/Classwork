nsides= int(input())

if nsides=='3':
    print('triangle')
elif nsides=='4':
    print('quadrilateral')
elif nsides=='5':
    print('pentagon')
elif nsides=='6':
    print('hexagon')
elif nsides=='7':
    print('heptagon')
elif nsides=='8':
    print('octagon')
elif nsides=='9':
    print('nonagon')
elif nsides=='10':
    print('decagon')
else:
    print('The number of sides is not supported')
